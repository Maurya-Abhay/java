package Array;

public class SimpleArray {
    public static void main(String[] args) {
        // 1D Array
        int[] numbers = new int[3];
        numbers[0] = 90;
        numbers[1] = 97;
        numbers[2] = 99;

        for (int i = 0; i<3; i++) {
            System.out.println(numbers[i]);
        }
    }
}
